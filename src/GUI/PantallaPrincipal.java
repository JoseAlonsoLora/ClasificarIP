    public void escribirEnPantallaInformacionIP(InformacionIP tipoIp, IP ip) {
        IPMetodos IpDao = new IPMetodos();
        switch (tipoIp) {
            case IPClaseA:
                TipoIP.setText("Clase A");
                break;
            case IPClaseB:
                TipoIP.setText("Clase B");
                break;
            case IPClaseC:
                TipoIP.setText("Clase C");
                break;
            default:
                TipoIP.setText("");
                break;
        }

        prefijo.setText(IpDao.calcularPrefijo(tipoIp));
        mascara.setText(IpDao.calcularMacara(tipoIp));
        direccionRed.setText(IpDao.calcularDireccionRed(ip, tipoIp));
        direccionBroadcast.setText(IpDao.calularDireccionBroadcast(ip, tipoIp));
    }

    public boolean validarCamposTexto() {
        boolean bandera = true;
        if (primerOcteto.getText().isEmpty() | segundoOcteto.getText().isEmpty() | tercerOcteto.getText().isEmpty()
                | cuartoOcteto.getText().isEmpty()) {
            bandera = false;
        }

        return bandera;
    }

