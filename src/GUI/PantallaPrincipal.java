    private void botonEvaluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEvaluarActionPerformed
        IP ip = new IP();
        IPMetodos IpDao = new IPMetodos();
        if (validarCamposTexto()) {
            ip.setOctetos(Integer.parseInt(primerOcteto.getText()));
            ip.setOctetos(Integer.parseInt(segundoOcteto.getText()));
            ip.setOctetos(Integer.parseInt(tercerOcteto.getText()));
            ip.setOctetos(Integer.parseInt(cuartoOcteto.getText()));

            switch (IpDao.validarIp(ip)) {
                case IPValida:
                    escribirEnPantallaInformacionIP(IpDao.determinarClaseIP(ip), ip);
                    break;
                case IPNoValida:
                    JOptionPane.showMessageDialog(null, "IP no válida");
                    break;
            }
        } else {
            JOptionPane.showMessageDialog(null, "Algunos de los campos estan vácios");
        }
    }//GEN-LAST:event_botonEvaluarActionPerformed

